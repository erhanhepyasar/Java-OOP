package oop5abstraction.abstractclassandinterface;

public class M extends B{

	@Override
	public void a() {
		System.out.println("I am A");
		
	}

	@Override
	public void b() {
		System.out.println("I am B");
		
	}

	@Override
	public void d() {
		System.out.println("I am D");
		
	}

}
